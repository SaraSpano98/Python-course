# A template for creating user-defined objects.

class MyClass:
    def __init__(self, a):
        self.a = a
    def __str__(self):
        return str(self.a)
    def __repr__(self):
        class_name = self.__class__.__name__
        return f'{class_name}({self.a!r})'

    @classmethod
    def get_class_name(cls):
        return cls.__name__

# Example usage:
obj = MyClass(1)
print(obj.a, str(obj), repr(obj))